static int check(struct TEST_VECTOR test)
{
    unsigned char scratch[1024];
    struct STREAM_STATE state;

    if (!prim_avail(test.primitive))
        return 1;

    ASSERT_SUCCESS(stream_init(&state, test.key, test.key_len,
                               test.primitive, 0));

    memcpy(scratch, test.input, test.input_len);

    stream_update(&state, scratch, test.input_len);

    stream_final(&state);

    ASSERT_BUF_EQ(scratch, test.expected, test.input_len);

    return 1;
}

static int check_generic(prim_t primitive)
{
    size_t t;

    for (t = 0; t < ARRAY_SIZE(tests); ++t)
        if (tests[t].primitive == primitive)
            if (!check(tests[t])) return 0;

    return 1;
}

int test_vectors_rc4(void);
int test_vectors_rc4(void)
{
    return check_generic(STREAM_RC4);
}
