int test_vectors_md5(void)
{
    size_t t;

    if (!prim_avail(HASH_MD5))
        return 1;

    for (t = 0; t < ARRAY_SIZE(tests); ++t)
        if (!check(tests + t)) return 0;

    return 1;
}
